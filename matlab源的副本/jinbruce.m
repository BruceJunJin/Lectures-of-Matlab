function jinbruce
[t,y] = ode45(@bruce,[0,10],[500000;2000;100]);
subplot(1,3,1),plot(t,y(:,1),'-');
subplot(1,3,2),plot(t,y(:,2),'-');
subplot(1,3,3),plot(t,y(:,3),'-');

function dy = bruce(t,y)
dy = [ 0.001*(1-0.5)*(y(1)+y(3))-0.01*y(1)*y(2)/(y(1)+y(2)+y(3))-0.001*y(1)+(1-0.6)*0.001*y(2);
      0.01*y(1)*y(2)/(y(1)+y(2)+y(3))+0.6*0.001*y(2)-(0.001+0.5+0.88)*y(2);
      0.5*y(2)-0.001*y(3)+0.5*0.001*(y(1)+y(3));
      ];